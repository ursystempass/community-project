                    Wayang Riders merupakan sebuah komunitas yang dibentuk pada 12 Februari tahun 2000,
                komunitas ini didirikan oleh Ricky Darmawan Noya berlokasi di Kota Cimahi,komunitas ini
                berasal dari salah satu tongkrongan yang ingin memamerkan motor motor mereka dengan cara
                membuat komunitas.Sekitar Tahun 2010-an,Komunitas Wayang Riders mulai terkenal,karena salah satu anggota
